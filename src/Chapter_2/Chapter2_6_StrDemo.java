package Chapter_2;
//Демонстрация управляющих последовательностей в символьных строках.
public class Chapter2_6_StrDemo {
	public static void main(String[] args) {
//		Управляющий литерал \n для перевода строки.
		System.out.println("Первая строка\nВторая строка");
//		Управляющий литерал \t (горизонт. табуляция) для выравнивания вывода.
		System.out.println("A\tB\tC");
		System.out.println("D\tE\tF");
	}
}
